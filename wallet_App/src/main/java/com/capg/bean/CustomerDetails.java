package com.capg.bean;

import java.time.LocalDate;

public class CustomerDetails {
	private String aadharNo;
	private String customerName;
	private int age;
	private String gender;
	private String address;
	private String nationality;
	private String mobileNo;
	private String custEmail;
	private String username;
	private String password;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getNationality() {
		return nationality;
	}

	public void setNationality(String nationality) {
		this.nationality = nationality;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getAadharNo() {
		return aadharNo;
	}

	public void setAadharNo(String aadharNo) {
		this.aadharNo = aadharNo;
	}

	public String getCustEmail() {
		return custEmail;
	}

	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}

	@Override
	public String toString() {
		return "customerDetails [aadharNo=" + aadharNo + ", customerName=" + customerName + ", age=" + age + ", gender="
				+ gender + ", address=" + address + ", nationality=" + nationality + ", mobileNo=" + mobileNo
				+ ", custEmail=" + custEmail + ", username=" + username + ", password=" + password + "]";
	}

}
